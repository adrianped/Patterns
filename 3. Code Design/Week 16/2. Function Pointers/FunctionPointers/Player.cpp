#include "Player.h"

//--------------------------------------------------------------------
//--------------------------------------------------------------------
Player::Player()
{
}

//--------------------------------------------------------------------
//--------------------------------------------------------------------
Player::~Player()
{
}

//--------------------------------------------------------------------
//--------------------------------------------------------------------
int Player::Add(int lhs, int rhs)
{
	return lhs + rhs;
}

//--------------------------------------------------------------------
//--------------------------------------------------------------------
int Player::Sub(int lhs, int rhs)
{
	return lhs - rhs;
}

//--------------------------------------------------------------------
//--------------------------------------------------------------------
Player::MathFunc Player::GetFunction(char opp)
{
	if(opp == '+')
	{
		return &Player::Add;
	}
	else if(opp == '-')
	{
		return &Player::Sub;
	}
	else
	{
		return nullptr;
	}
}

